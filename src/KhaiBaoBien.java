public class KhaiBaoBien {
    public static void main(String[] args) {
        int i = 20; // khai báo i là kiêu số nguyên có giá trị là 10
        float f = 20.5f; // khai báo biến f có giá trị là 20.5 kiểu float
        boolean b= true; // khai báo biến b có kiểu giữ liệu boolean có giá trị là true
        char c = 'a';// khai báo biến C có kiểu là kí tự có giá trị là kí tự a , kí tự bằng dấu ''
        String s = "Hà nội" ; // khai báo biến s có kiểu là string c giá trị là chuỗi "hà nội " , string khai báo bằng dấu ""
        System.out.println("i ="+i);
        System.out.println("f ="+f);
        System.out.println("b ="+b);
        System.out.println("c ="+c);
        System.out.println("s ="+s);

        }


    }

